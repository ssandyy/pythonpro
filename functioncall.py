#             by variable length argument..........
#==============================================================================================================================

def add(*n):
    sum=0
    for i in n:
        sum=sum+i
    print(sum)


add(10,20,30)
add(11,22,33,44,55)
add(1,2,3,4,5,6,7,6,5,4,3,2,11,22,33,44,55,66,77,88)